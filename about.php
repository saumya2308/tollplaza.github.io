<h1>ONLINE FREELANCING PLATFORM</h1>
<h2>INTRODUCTION</h2>
<h3>The “Online Freelancing Platform” is a platform for hiring the freelancer and pay at the same portal. In this, there will be two portals: one for freelancers where any person can create a freelancing account and another one will be a client where clients can create their account and hire the freelancers by posting a project.
When a client will create an account with our portal then he will post a project as per his requirements.When a freelancer will create an account with our portal then they will bid for the project present on the site and based on their skills.
After bidding for the project clients will get the notification and he needs to accept or reject the bid for further work. When the client will accept the bid then they both will get a notification and will start working on it
When the project is completed freelancers will submit the project to us and we will send it to the client. After the project approval from the client, they need to pay us and we will pay freelancers.</h3>

<h2>MAIN POINT</h2>
<h3>The purpose of “Online Freelancing Platform” is to automate the existing manual system by the help of computerized equipment and full-fledged computer software, fulfilling their requirements, so that their valuable data or information can be stored for a longer period with easy accessing and manipulation of the same. The required software and hardware are easily available and easy to work with.

This system, as described above, can lead to error free, secure, reliable and fast management systems. It can assist the user to concentrate on their other activities rather than to concentrate on the freelancing work. This will help the organization in better utilization of resources. The organization can maintain computerized records without redundant entries. That means that one need not be distracted by information that is not relevant, while being able to reach the information.</h3>
